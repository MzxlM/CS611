    private JPanel contentPane;
    private JButton buttonOK;
    private JButton buttonCancel;
    private JSpinner withdrawAmount;
    private JLabel amountLabel;
    private JLabel withdrawRuleLabel;
    private ATM atmsystem;
    private BankAccount bankAccount;

    public WithdrawDialog(ATM atm, BankAccount bankAccount) {

        this.atmsystem = atm;
        this.bankAccount = bankAccount;
        withdrawRuleLabel.setText("Each withdrawal will charge $" + Constants.Withdraw_Fee);
        withdrawAmount.setModel(new SpinnerNumberModel(10, 10, bankAccount.getBalance(), 0.1));
        setContentPane(contentPane);
        setModal(true);
        getRootPane().setDefaultButton(buttonOK);

        buttonOK.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onOK();
            }
        });

        buttonCancel.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onCancel();
            }
        });

        // call onCancel() when cross is clicked
        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                onCancel();
            }
        });

        // call onCancel() on ESCAPE
        contentPane.registerKeyboardAction(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onCancel();
            }
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);

        setLocationRelativeTo(null);
        setSize(500, 200);
        setVisible(true);
    }

    private void onOK() {

        double value = ((Number) withdrawAmount.getValue()).doubleValue();
        if (!atmsystem.withDraw(bankAccount.getAccountID(), value)) {
            System.out.println("Failed to withdraw");
        }
        dispose();
    }

